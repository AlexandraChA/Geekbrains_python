import sys
#Task 1 from 1 lesson
num = int(input('Введите трехзначное число: '))
if 100 <= num < 1000:
    n1 = num // 100
    n2 = (num % 100) // 10
    n3 = num % 10
    print(f'Сумма трех цифр равна {n1+n2+n3}')
    print(f'Произведение трех цифр равно {n1*n2*n3}')
else:
    print('Необходимо ввести трехзначное число')
s = sys.getsizeof(num)+sys.getsizeof(n1)+sys.getsizeof(n2)+sys.getsizeof(n3)
print(f'При входных данных {num} объем использованной памяти будет равен {s} байт')
#При входных данных 345 объем использованной памяти будет равен 112 байт
#При входных данных 885 объем использованной памяти будет равен 112 байт 

